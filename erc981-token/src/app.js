// Create contract instance
const contract = new web3.eth.Contract(contractABI, contractAddress);

// Connect wallet
document.getElementById('connectWallet').addEventListener('click', async () => {
  try {
    await window.ethereum.request({ method: 'eth_requestAccounts' });
    document.getElementById('connectWallet').style.display = 'none';
    document.getElementById('transferForm').style.display = 'block';
  } catch (error) {
    showError('Failed to connect wallet. Please check your MetaMask extension.');
  }
});

// Transfer ownership
document.getElementById('transferOwnership').addEventListener('click', async () => {
  const tokenId = document.getElementById('tokenId').value;
  const newOwners = document.getElementById('newOwners').value.split(',');
  const newShares = document.getElementById('newShares').value.split(',').map(Number);

  if (!tokenId || !newOwners.length || !newShares.length || newShares.reduce((a, b) => a + b, 0) !== 100) {
    showError('Please enter valid token ID, new owners, and new shares (must sum to 100).');
    return;
  }

  try {
    const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
    await contract.methods.transferOwnership(tokenId, newOwners, newShares).send({ from: accounts[0] });
    showSuccess('Ownership transferred successfully!');
  } catch (error) {
    showError(`Error transferring ownership: ${error.message}`);
  }
});

function showError(message) {
  document.getElementById('result').textContent = message;
  document.getElementById('result').style.color = 'red';
}

function showSuccess(message) {
  document.getElementById('result').textContent = message;
  document.getElementById('result').style.color = 'green';
}
